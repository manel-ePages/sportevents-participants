CREATE TABLE `participant` (
  `id` int(11) NOT NULL AUTO_INCREMENT COMMENT 'identificador numèric del corredor',
  `data_registre` datetime NOT NULL COMMENT 'data de creació del registre',
  `data_ultima_modificacio` datetime NOT NULL COMMENT 'data del últim canvi del registre',
  `nif` varchar(11) COLLATE utf8_spanish_ci NOT NULL COMMENT 'codi identificació fiscal',
  `data_naixement` date NOT NULL COMMENT 'data de naixement del participant',
  `nom` varchar(25) COLLATE utf8_spanish_ci NOT NULL COMMENT 'nom del participant',
  `cognom1` varchar(25) COLLATE utf8_spanish_ci NOT NULL COMMENT 'primer cognom del participant',
  `cognom2` varchar(25) COLLATE utf8_spanish_ci DEFAULT NULL COMMENT 'segón cognom del participant',
  `genere` enum('Home','Dona') COLLATE utf8_spanish_ci NOT NULL COMMENT 'gènere del participant',
  `adreça` varchar(80) COLLATE utf8_spanish_ci NOT NULL COMMENT 'adreça on viu el participant (carrer, número, pis...)',
  `codi_postal` varchar(7) COLLATE utf8_spanish_ci NOT NULL COMMENT 'codi postal de l''adreça del participant',
  `poblacio` varchar(45) COLLATE utf8_spanish_ci NOT NULL COMMENT 'nom de la població corresponent a l''adreça postal del participant',
  `pais` varchar(25) COLLATE utf8_spanish_ci DEFAULT NULL COMMENT 'païs de l''adreça del participant',
  `telefon` varchar(15) COLLATE utf8_spanish_ci DEFAULT NULL COMMENT 'telèfon del participant',
  `telefon_urgencia` varchar(15) COLLATE utf8_spanish_ci DEFAULT NULL COMMENT 'telèfon en cas de urgència durant la cursa',
  `email` varchar(45) COLLATE utf8_spanish_ci DEFAULT NULL COMMENT 'correu electrònic del participant',
  `actiu` int(1) NOT NULL COMMENT 'estat de les dades del participant. Actives o inactives',
  PRIMARY KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_spanish_ci COMMENT='dades dels corredors que s''han inscrit en alguna cursa';